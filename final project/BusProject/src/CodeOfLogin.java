
public class CodeOfLogin {
    
    
    private String user;
    private String pass;
    
    public String getName()
    {
        return user;
    }
    
    public String getPass()
    {
        return pass;
    }
    
    public void setPass(String pass)
    {
        this.pass = pass;
    }
    
    public void setName(String user)
    {
        this.user = user;
    }
}
